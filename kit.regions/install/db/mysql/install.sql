CREATE TABLE IF NOT EXISTS `kit_regions_options` (
	`CODE` VARCHAR(50) not null,
	`VALUE` TEXT,
	`SITE_ID` CHAR(2),
	UNIQUE ix_option(CODE, SITE_ID),
	INDEX ix_option_name(CODE)
);
CREATE TABLE IF NOT EXISTS `kit_regions` (
  `ID`  int(11) NOT NULL AUTO_INCREMENT ,
	`CODE` VARCHAR(100),
	`NAME` VARCHAR(255) not null,
	`SORT` int(18) NOT NULL DEFAULT '100',
	`SITE_ID` VARCHAR(255),
	`PRICE_CODE` text,
	`STORE` text,
	`COUNTER` text,
	`MAP_YANDEX` text,
	`MAP_GOOGLE` text,
  `MANAGER` int(11),
  `PRICE_VALUE_TYPE` VARCHAR(255),
  `PRICE_VALUE` int(18),
  `DEFAULT_DOMAIN` VARCHAR(1),
  PRIMARY KEY (`ID`)
);
CREATE TABLE IF NOT EXISTS `kit_regions_fields` (
  `ID`  int(11) NOT NULL AUTO_INCREMENT ,
	`ID_REGION` int(11),
	`CODE` VARCHAR(255) not null,
	`VALUE` text,
  PRIMARY KEY (`ID`)
);
